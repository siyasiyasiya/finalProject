package com.example.demo;

public class Locations {
    private int x;
    private int y;

    public Locations(int xx, int yy){
        x = xx;
        y = yy;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}